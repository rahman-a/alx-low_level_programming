/**
 * op_add - a function to add two numbers
 * @a: first integer
 * @b: second integer
 * Return: integer
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub -  a function to substract two integer
 * @a: first integer
 * @b: second integer
 * Return: integer
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - a function to multiply two integers
 * @a: integer
 * @b: integer
 * Return: integer
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - a function to divide two integer
 * @a: integer
 * @b: integer
 * Return: integer
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - a function the find the remainder of integer
 * @a: integer
 * @b: integer
 * Return: remainder integer
*/

int op_mod(int a, int b)
{
	return (a % b);
}
